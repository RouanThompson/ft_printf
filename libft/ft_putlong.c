#include "libft.h"
#include <limits.h>

void		ft_putlong(long n)
{
	if (n < 0)
	{
		ft_putchar('-');
		if (n == LONG_MIN)
			return (ft_putstr("9223372036854775808"));
		n = n * -1;
	}
	if (n >= 10)
		ft_putlong(n / 10);
	ft_putchar((n % 10) + '0');
}
